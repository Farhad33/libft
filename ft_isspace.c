#include "libft.h"

int		ft_isspace(int c)
{
	if (c == ' ' ||
		c == '\r' ||
		c == '\f' ||
		c == '\t' ||
		c == '\v' ||
		c == '\n')
	{
		return (1);
	}
	return (0);
}
