#include "libft.h"

int		ft_count_words(char const *s, char c)
{
	int		j;
	int		i;

	i = 0;
	j = 0;
	while (s[i])
	{
		if (s[i] != c && s[i])
		{
			while (s[i] != c && s[i])
				i++;
			j++;
			i--;
		}
		i++;
	}
	return (j);
}
